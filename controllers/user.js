/**
 * POST /forgot
 * Create a random token, then the send user an email with a reset link.
 */
exports.postForgot = (req, res, next) => {
  req.assert('email', 'Per favore inserisci un indirizzo email valido.').isEmail();
  req.sanitize('email').normalizeEmail({ gmail_remove_dots: false });

  const errors = req.validationErrors();

  if (errors) {
    req.flash('errors', errors);
    return res.redirect('/forgot');
  }

  const createRandomToken = crypto
    .randomBytesAsync(16)
    .then(buf => buf.toString('hex'));

  const setRandomToken = token =>
    User
      .findOne({ email: req.body.email })
      .then((user) => {
        if (!user) {
          req.flash('errors', { msg: 'Non è presente alcun account con l\'indirizzo email fornito' });
        } else {
          user.passwordResetToken = token;
          user.passwordResetExpires = Date.now() + 3600000; // 1 hour
          user = user.save();
        }
        return user;
      });

  const sendForgotPasswordEmail = (user) => {
    if (!user) { return; }
    const token = user.passwordResetToken;
    const transporter = nodemailer.createTransport({
      service: 'SendGrid',
      auth: {
        user: process.env.SENDGRID_USER,
        pass: process.env.SENDGRID_PASSWORD
      }
    });
    const mailOptions = {
      to: user.email,
      from: 'info@jpp.it',
      subject: 'Cambio della password al sito JuniorPhotoPlanet - Backoffice',
      text: `Ricevi questa comunicazione perchè tu (o qualcuno per tuo conto) ha richiesto il reset della password per il tuo account.\n\n
        Per favore clicca sul seguente link, oppure copia/incolla sul browser:\n\n
        http://${req.headers.host}/reset/${token}\n\n
        Se non hai richiesto questo reset, per cortesia ignora questa email e la tua password rimarrà inalterata.\n`
    };
    return transporter.sendMail(mailOptions)
      .then(() => {
        req.flash('info', { msg: `Una comunicazione email è stata inviata a ${user.email} con ulteriori istruzioni.` });
      });
  };

  createRandomToken
    .then(setRandomToken)
    .then(sendForgotPasswordEmail)
    .then(() => res.redirect('/forgot'))
    .catch(next);
};
